#include <stdio.h>

int main (void){
	
	/* RELATION OPERATORS **/

	int a = 2 > 3;  // 0
	int b = 2 < 3; // 1
	int c = 2 == 3; // 0
	int d = 2 >= 2; // 1
	int e = 2 <= 3; // 1
	int f = 2 != 3;  // 1





	printf("a = %d-\nb = :%d-\nc %d\nd =: %d\ne = %d\nf = :%d\n", a, b, c, d, e, f);


	return 0;
}
